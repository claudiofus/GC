package gc.model.types;

public class ProjectCost {
	private float manpower;
	private float vehicle;
	private float equipment;
	private float materials;
	private float profit;
	private float others;
	private float total;

	public float getManpower() {
		return manpower;
	}

	public void setManpower(float manpower) {
		this.manpower = manpower;
	}

	public float getVehicle() {
		return vehicle;
	}

	public void setVehicle(float vehicle) {
		this.vehicle = vehicle;
	}

	public float getEquipment() {
		return equipment;
	}

	public void setEquipment(float equipment) {
		this.equipment = equipment;
	}

	public float getMaterials() {
		return materials;
	}

	public void setMaterials(float materials) {
		this.materials = materials;
	}

	public float getProfit() {
		return profit;
	}

	public void setProfit(float profit) {
		this.profit = profit;
	}

	public float getOthers() {
		return others;
	}

	public void setOthers(float others) {
		this.others = others;
	}

	public float getTotal() {
		return total;
	}

	public void setTotal(float total) {
		this.total = total;
	}
}
